public class pg15 {
    public static void main(String[] args) {
        //&&
        System.out.println((3>2) && (5>0));
        System.out.println((3<2) && (5>0));
        System.out.println((3>2) && (5<0));
        System.out.println((3>2) && (5>0));
        //||
        System.out.println((8<9)||(2>0));
        System.out.println((8<9)||(2<0));
        System.out.println((8>9)||(2>0));
        System.out.println((8<9)||(2>0));
        //Logical nOT !
        System.out.println(!(1>0));

    }
}
